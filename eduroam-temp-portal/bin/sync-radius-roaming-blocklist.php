<?php
declare(strict_types=1);

require __DIR__ . '/../app/bootstrap.php';

const ROAMING_BLOCKLIST_FILE = '/etc/raddb/mods-config/files/ncut-roaming-blocklist';
const RADIUS_AUTHORIZE_FILE = '/etc/raddb/mods-config/files/authorize';
const ROAMING_INCLUDE_LINE = '$INCLUDE ' . ROAMING_BLOCKLIST_FILE;

function require_root(): void
{
    if (function_exists('posix_geteuid') && posix_geteuid() !== 0) {
        throw new RuntimeException('This helper must run as root.');
    }
}

function shell_join(array $args): string
{
    return implode(' ', array_map('escapeshellarg', $args));
}

function command_path(array $candidates): string
{
    foreach ($candidates as $candidate) {
        if (is_executable($candidate)) {
            return $candidate;
        }
    }
    throw new RuntimeException('Required command not found: ' . implode(', ', $candidates));
}

function run_command(string $label, array $args, bool $required = true, int $timeout = 60): array
{
    $timeoutBin = is_executable('/usr/bin/timeout') ? '/usr/bin/timeout' : '';
    $cmd = $timeoutBin !== ''
        ? escapeshellarg($timeoutBin) . ' ' . escapeshellarg((string) $timeout) . ' ' . shell_join($args)
        : shell_join($args);
    $descriptors = [
        0 => ['pipe', 'r'],
        1 => ['pipe', 'w'],
        2 => ['pipe', 'w'],
    ];
    $process = proc_open($cmd, $descriptors, $pipes, '/');
    if (!is_resource($process)) {
        throw new RuntimeException($label . ' could not start.');
    }
    fclose($pipes[0]);
    $stdout = stream_get_contents($pipes[1]);
    fclose($pipes[1]);
    $stderr = stream_get_contents($pipes[2]);
    fclose($pipes[2]);
    $exitCode = proc_close($process);
    $output = trim(($stdout ?: '') . "\n" . ($stderr ?: ''));
    if ($required && $exitCode !== 0) {
        throw new RuntimeException($label . " failed.\n" . $output);
    }
    return [
        'label' => $label,
        'exit' => $exitCode,
        'output' => $output,
    ];
}

function atomic_write(string $target, string $content, int $mode): void
{
    $dir = dirname($target);
    if (!is_dir($dir) && !mkdir($dir, 0755, true) && !is_dir($dir)) {
        throw new RuntimeException('Unable to create directory: ' . $dir);
    }
    $tmp = $target . '.ncut.tmp';
    if (file_put_contents($tmp, $content, LOCK_EX) === false) {
        throw new RuntimeException('Unable to write ' . $tmp);
    }
    chmod($tmp, $mode);
    if (!rename($tmp, $target)) {
        throw new RuntimeException('Unable to replace ' . $target);
    }
}

function backup_once(string $target): void
{
    if (!is_file($target)) {
        return;
    }
    $backup = $target . '.pre-ncut-roaming';
    if (!file_exists($backup) && !copy($target, $backup)) {
        throw new RuntimeException('Unable to create backup: ' . $backup);
    }
}

function radius_string(string $value): string
{
    $value = preg_replace('/[\r\n]+/', ' ', $value) ?? $value;
    $value = str_replace(['\\', '"'], ['\\\\', '\\"'], $value);
    return '"' . $value . '"';
}

function radius_mac_regex(string $value): string
{
    $hex = preg_replace('/[^0-9a-f]/', '', strtolower($value)) ?? '';
    if (strlen($hex) !== 12) {
        throw new RuntimeException('Invalid Calling-Station-Id value: ' . $value);
    }
    $parts = str_split($hex, 2);
    return radius_string('(?i)^' . implode('[-:]?', array_map(static fn($part) => preg_quote($part, '/'), $parts)) . '$');
}

function blocklist_content(array $rows): string
{
    $lines = [
        '# Generated by NCUT eduroam portal. Do not edit by hand.',
        '# Source table: radius_roaming_blocklist',
        '# This file is included by ' . RADIUS_AUTHORIZE_FILE,
        '',
    ];
    foreach ($rows as $row) {
        $type = (string) $row['block_type'];
        $value = (string) $row['block_value'];
        $reason = trim((string) ($row['reason'] ?? ''));
        $label = $reason !== '' ? mb_substr($reason, 0, 160) : 'Blocked by NCUT eduroam policy';
        $reply = radius_string('Blocked by NCUT eduroam policy');
        $comment = '# id=' . (int) $row['id'] . ' type=' . $type . ' value=' . $value;
        if ($reason !== '') {
            $comment .= ' reason=' . str_replace(["\r", "\n"], ' ', mb_substr($reason, 0, 120));
        }
        $lines[] = $comment;
        if ($type === 'username') {
            $lines[] = 'DEFAULT User-Name =~ ' . radius_string('(?i)^' . preg_quote($value, '/') . '$') . ', Auth-Type := Reject';
        } elseif ($type === 'realm') {
            $lines[] = 'DEFAULT User-Name =~ ' . radius_string('(?i)@' . preg_quote($value, '/') . '$') . ', Auth-Type := Reject';
        } elseif ($type === 'calling_station_id') {
            $lines[] = 'DEFAULT Calling-Station-Id =~ ' . radius_mac_regex($value) . ', Auth-Type := Reject';
        } else {
            throw new RuntimeException('Unknown block type: ' . $type);
        }
        $lines[] = '        Reply-Message := ' . $reply;
        $lines[] = '';
    }
    if (count($rows) === 0) {
        $lines[] = '# No active roaming block entries.';
    }
    return implode("\n", $lines) . "\n";
}

function ensure_authorize_include(string $authorizeContent): string
{
    if (str_contains($authorizeContent, ROAMING_INCLUDE_LINE)) {
        return $authorizeContent;
    }
    return "# NCUT roaming blocklist include\n" . ROAMING_INCLUDE_LINE . "\n\n" . $authorizeContent;
}

require_root();
$pdo = db();
$rows = roaming_blocklist($pdo, false);
$content = blocklist_content($rows);

$oldBlocklist = is_file(ROAMING_BLOCKLIST_FILE) ? (string) file_get_contents(ROAMING_BLOCKLIST_FILE) : null;
if (!is_readable(RADIUS_AUTHORIZE_FILE)) {
    throw new RuntimeException('Unable to read ' . RADIUS_AUTHORIZE_FILE);
}
$oldAuthorize = (string) file_get_contents(RADIUS_AUTHORIZE_FILE);
$newAuthorize = ensure_authorize_include($oldAuthorize);

backup_once(RADIUS_AUTHORIZE_FILE);
backup_once(ROAMING_BLOCKLIST_FILE);

try {
    atomic_write(ROAMING_BLOCKLIST_FILE, $content, 0640);
    @chgrp(ROAMING_BLOCKLIST_FILE, 'radiusd');
    if ($newAuthorize !== $oldAuthorize) {
        atomic_write(RADIUS_AUTHORIZE_FILE, $newAuthorize, 0640);
        @chgrp(RADIUS_AUTHORIZE_FILE, 'radiusd');
    }

    $radiusd = command_path(['/usr/sbin/radiusd', '/usr/sbin/freeradius']);
    run_command('FreeRADIUS config check', [$radiusd, '-XC'], true, 45);
    $systemctl = command_path(['/usr/bin/systemctl', '/bin/systemctl']);
    run_command('Restart FreeRADIUS', [$systemctl, 'restart', 'radiusd'], true, 60);
    $pdo->exec(
        'UPDATE radius_roaming_blocklist
         SET last_synced_at = NOW()
         WHERE enabled = 1
           AND (blocked_until IS NULL OR blocked_until > NOW())'
    );
} catch (Throwable $e) {
    if ($oldBlocklist !== null) {
        atomic_write(ROAMING_BLOCKLIST_FILE, $oldBlocklist, 0640);
    } else {
        atomic_write(ROAMING_BLOCKLIST_FILE, "# Generated by NCUT eduroam portal.\n# Sync failed and no previous blocklist existed.\n", 0640);
    }
    atomic_write(RADIUS_AUTHORIZE_FILE, $oldAuthorize, 0640);
    @chgrp(ROAMING_BLOCKLIST_FILE, 'radiusd');
    @chgrp(RADIUS_AUTHORIZE_FILE, 'radiusd');
    throw $e;
}

echo 'active_roaming_blocks=' . count($rows) . PHP_EOL;
echo 'radiusd_restarted=1' . PHP_EOL;
