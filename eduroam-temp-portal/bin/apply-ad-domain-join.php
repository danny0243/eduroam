<?php
declare(strict_types=1);

require __DIR__ . '/../app/bootstrap.php';

function require_root(): void
{
    if (function_exists('posix_geteuid') && posix_geteuid() !== 0) {
        throw new RuntimeException('This helper must run as root.');
    }
}

function host_list(string $hosts): array
{
    $items = preg_split('/[,;\s]+/', $hosts) ?: [];
    $items = array_values(array_filter(array_map('trim', $items), static fn($item) => $item !== ''));
    foreach ($items as $item) {
        if (!preg_match('/^[a-zA-Z0-9_.:-]+$/', $item)) {
            throw new RuntimeException('Invalid domain controller host: ' . $item);
        }
    }
    return $items;
}

function shell_join(array $args): string
{
    return implode(' ', array_map('escapeshellarg', $args));
}

function command_path(array $candidates): string
{
    foreach ($candidates as $candidate) {
        if (is_executable($candidate)) {
            return $candidate;
        }
    }
    throw new RuntimeException('Required command not found: ' . implode(', ', $candidates));
}

function run_command(string $label, array $args, string $stdin = '', bool $required = true, int $timeout = 60): array
{
    $timeoutBin = is_executable('/usr/bin/timeout') ? '/usr/bin/timeout' : '';
    $cmd = $timeoutBin !== ''
        ? escapeshellarg($timeoutBin) . ' ' . escapeshellarg((string) $timeout) . ' ' . shell_join($args)
        : shell_join($args);
    $descriptors = [
        0 => ['pipe', 'r'],
        1 => ['pipe', 'w'],
        2 => ['pipe', 'w'],
    ];
    $process = proc_open($cmd, $descriptors, $pipes, '/');
    if (!is_resource($process)) {
        throw new RuntimeException($label . ' could not start.');
    }
    fwrite($pipes[0], $stdin);
    fclose($pipes[0]);
    $stdout = stream_get_contents($pipes[1]);
    fclose($pipes[1]);
    $stderr = stream_get_contents($pipes[2]);
    fclose($pipes[2]);
    $exitCode = proc_close($process);
    $output = trim(($stdout ?: '') . "\n" . ($stderr ?: ''));
    if ($required && $exitCode !== 0) {
        throw new RuntimeException($label . " failed.\n" . $output);
    }
    return [
        'label' => $label,
        'exit' => $exitCode,
        'output' => $output,
    ];
}

function atomic_write(string $target, string $content, int $mode): void
{
    $dir = dirname($target);
    if (!is_dir($dir) && !mkdir($dir, 0755, true) && !is_dir($dir)) {
        throw new RuntimeException('Unable to create directory: ' . $dir);
    }
    $tmp = $target . '.ncut.tmp';
    if (file_put_contents($tmp, $content, LOCK_EX) === false) {
        throw new RuntimeException('Unable to write ' . $tmp);
    }
    chmod($tmp, $mode);
    if (!rename($tmp, $target)) {
        throw new RuntimeException('Unable to replace ' . $target);
    }
}

function backup_once(string $target): void
{
    if (!is_file($target)) {
        return;
    }
    $backup = $target . '.pre-ncut';
    if (!file_exists($backup) && !copy($target, $backup)) {
        throw new RuntimeException('Unable to create backup: ' . $backup);
    }
}

function krb5_config(string $realm, string $domain, array $hosts): string
{
    $lines = [
        '# Generated by NCUT eduroam portal. Do not edit by hand.',
        '[libdefaults]',
        '    default_realm = ' . $realm,
        '    dns_lookup_realm = false',
        '    dns_lookup_kdc = false',
        '    rdns = false',
        '    ticket_lifetime = 24h',
        '    forwardable = true',
        '',
        '[realms]',
        '    ' . $realm . ' = {',
    ];
    foreach ($hosts as $host) {
        $lines[] = '        kdc = ' . $host;
    }
    $lines[] = '        admin_server = ' . $hosts[0];
    $lines[] = '    }';
    $lines[] = '';
    $lines[] = '[domain_realm]';
    $lines[] = '    .' . $domain . ' = ' . $realm;
    $lines[] = '    ' . $domain . ' = ' . $realm;
    $lines[] = '';
    return implode("\n", $lines);
}

function samba_config(string $realm, string $workgroup, array $hosts): string
{
    return implode("\n", [
        '# Generated by NCUT eduroam portal. Do not edit by hand.',
        '[global]',
        '    workgroup = ' . $workgroup,
        '    security = ads',
        '    realm = ' . $realm,
        '    password server = ' . implode(' ', $hosts),
        '    server role = member server',
        '    dedicated keytab file = /etc/krb5.keytab',
        '    kerberos method = secrets and keytab',
        '    winbind use default domain = yes',
        '    winbind offline logon = no',
        '    winbind enum users = no',
        '    winbind enum groups = no',
        '    winbind refresh tickets = yes',
        '    idmap config * : backend = tdb',
        '    idmap config * : range = 10000-199999',
        '    template shell = /sbin/nologin',
        '    template homedir = /home/%D/%U',
        '    client min protocol = SMB2',
        '',
    ]);
}

function ad_login_name(string $username, string $domain, string $upnSuffix): string
{
    $username = trim($username);
    $upnSuffix = strtolower($upnSuffix !== '' ? $upnSuffix : $domain);
    if (str_contains($username, '\\')) {
        $parts = explode('\\', $username, 2);
        $username = $parts[1];
    }
    $lower = strtolower($username);
    foreach (array_unique([$upnSuffix, $domain]) as $suffix) {
        if ($suffix !== '' && str_ends_with($lower, '@' . strtolower($suffix))) {
            return substr($username, 0, -1 * (strlen($suffix) + 1));
        }
    }
    return $username;
}

function print_optional_result(string $label, array $result): void
{
    if ((int) $result['exit'] === 0) {
        echo $label . ": OK\n";
        return;
    }
    echo $label . ': skipped or warning';
    if ($result['output'] !== '') {
        echo ' - ' . mb_substr((string) $result['output'], 0, 180);
    }
    echo "\n";
}

try {
    require_root();
    $payload = json_decode(stream_get_contents(STDIN), true);
    if (!is_array($payload)) {
        $payload = [];
    }

    $settings = ad_settings(db());
    if (!$settings['enabled'] || $settings['mode'] !== 'winbind') {
        throw new RuntimeException('AD winbind mode is not enabled in portal settings.');
    }
    if ($settings['domain'] === '' || $settings['netbios_domain'] === '' || $settings['hosts'] === '') {
        throw new RuntimeException('AD domain, NetBIOS domain, and domain controller are required.');
    }
    if (!preg_match('/^[a-z0-9.-]+$/i', $settings['domain'])) {
        throw new RuntimeException('Invalid AD domain.');
    }
    if (!preg_match('/^[A-Z0-9_-]+$/', $settings['netbios_domain'])) {
        throw new RuntimeException('Invalid NetBIOS domain.');
    }

    $hosts = host_list($settings['hosts']);
    if (!$hosts) {
        throw new RuntimeException('No domain controller host configured.');
    }
    $realm = strtoupper($settings['domain']);
    $workgroup = strtoupper($settings['netbios_domain']);
    $joinUsername = trim((string) ($payload['join_username'] ?? ''));
    $joinPassword = (string) ($payload['join_password'] ?? '');
    $testUsername = trim((string) ($payload['test_username'] ?? ''));
    $testPassword = (string) ($payload['test_password'] ?? '');

    $systemctl = command_path(['/usr/bin/systemctl', '/bin/systemctl']);
    $net = command_path(['/usr/bin/net', '/bin/net']);
    $wbinfo = command_path(['/usr/bin/wbinfo', '/bin/wbinfo']);
    $php = command_path(['/usr/bin/php', '/usr/local/bin/php']);

    backup_once('/etc/samba/smb.conf');
    atomic_write('/etc/krb5.conf.d/ncut-ad.conf', krb5_config($realm, $settings['domain'], $hosts), 0644);
    atomic_write('/etc/samba/smb.conf', samba_config($realm, $workgroup, $hosts), 0644);
    echo "Kerberos/Samba config: OK\n";

    if (is_executable('/usr/bin/testparm')) {
        run_command('Validate Samba config', ['/usr/bin/testparm', '-s'], '', true, 30);
        echo "Samba config validation: OK\n";
    }

    $testJoin = run_command('Check existing domain join', [$net, 'ads', 'testjoin'], '', false, 30);
    $joined = ((int) $testJoin['exit'] === 0);
    if (!$joined || $joinUsername !== '' || $joinPassword !== '') {
        if ($joinUsername === '' || $joinPassword === '') {
            throw new RuntimeException('Server is not joined to AD yet. Please provide domain join username and password.');
        }
        run_command('Join AD domain', [$net, 'ads', 'join', '-U', $joinUsername], $joinPassword . "\n", true, 90);
        echo "AD domain join: OK\n";
    } else {
        echo "AD domain join: already joined\n";
    }

    run_command('Start winbind', [$systemctl, 'enable', '--now', 'winbind'], '', true, 60);
    echo "winbind service: OK\n";

    $getent = command_path(['/usr/bin/getent', '/bin/getent']);
    $groupCheck = run_command('Check wbpriv group', [$getent, 'group', 'wbpriv'], '', false, 10);
    if ((int) $groupCheck['exit'] === 0 && is_executable('/usr/sbin/usermod')) {
        run_command('Allow radiusd to access winbind privileged pipe', ['/usr/sbin/usermod', '-a', '-G', 'wbpriv', 'radiusd'], '', false, 20);
    }
    if (is_dir('/var/lib/samba/winbindd_privileged')) {
        $group = (int) $groupCheck['exit'] === 0 ? 'wbpriv' : 'radiusd';
        run_command('Set winbind privileged pipe group', ['/usr/bin/chgrp', $group, '/var/lib/samba/winbindd_privileged'], '', false, 20);
        run_command('Set winbind privileged pipe mode', ['/usr/bin/chmod', '750', '/var/lib/samba/winbindd_privileged'], '', false, 20);
    }

    run_command('Sync FreeRADIUS AD settings', [$php, '/var/www/eduroam-portal/bin/sync-radius-ad-settings.php'], '', true, 30);
    run_command('Restart FreeRADIUS', [$systemctl, 'restart', 'radiusd'], '', true, 60);
    echo "FreeRADIUS sync/restart: OK\n";

    run_command('Verify domain join', [$net, 'ads', 'testjoin'], '', true, 30);
    echo "Domain join test: OK\n";
    run_command('Verify winbind trust', [$wbinfo, '-t'], '', true, 30);
    echo "Winbind trust test: OK\n";

    if ($testUsername !== '' && $testPassword !== '') {
        $ntlmAuth = is_executable($settings['ntlm_auth_path'])
            ? $settings['ntlm_auth_path']
            : command_path(['/usr/bin/ntlm_auth', '/bin/ntlm_auth']);
        $loginName = ad_login_name($testUsername, $settings['domain'], $settings['upn_suffix']);
        run_command(
            'Verify AD password with ntlm_auth',
            [$ntlmAuth, '--helper-protocol=squid-2.5-basic', '--domain=' . $workgroup],
            $loginName . ' ' . $testPassword . "\n",
            true,
            30
        );
        echo "AD password test: OK\n";
    }
} catch (Throwable $e) {
    fwrite(STDERR, $e->getMessage() . PHP_EOL);
    exit(1);
}
