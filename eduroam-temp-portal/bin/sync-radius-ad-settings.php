<?php
declare(strict_types=1);

require __DIR__ . '/../app/bootstrap.php';

$settings = ad_settings(db());
$enabled = $settings['enabled']
    && $settings['mode'] === 'winbind'
    && $settings['netbios_domain'] !== ''
    && $settings['ntlm_auth_path'] !== '';

$content = "# Generated by NCUT eduroam portal. Do not edit by hand.\n";
$content .= 'AD_ENABLED=' . ($enabled ? '1' : '0') . "\n";
$content .= 'AD_DOMAIN=' . escapeshellarg($settings['domain']) . "\n";
$content .= 'AD_NETBIOS_DOMAIN=' . escapeshellarg($settings['netbios_domain']) . "\n";
$content .= 'AD_UPN_SUFFIX=' . escapeshellarg($settings['upn_suffix'] !== '' ? $settings['upn_suffix'] : $settings['domain']) . "\n";
$content .= 'AD_NTLM_AUTH_PATH=' . escapeshellarg($settings['ntlm_auth_path']) . "\n";

$target = '/etc/raddb/ncut-ad.conf';
$tmp = $target . '.tmp';
if (file_put_contents($tmp, $content, LOCK_EX) === false) {
    throw new RuntimeException('Unable to write ' . $tmp);
}
chmod($tmp, 0640);
rename($tmp, $target);
@chgrp($target, 'radiusd');

echo 'ad_radius_enabled=' . ($enabled ? '1' : '0') . PHP_EOL;
